const escola = "Cod3r"
console.log(escola.charAt(4)) //número da letra
console.log(escola.charAt(7))
console.log(escola.charCodeAt(3)) // codigo ascii
console.log(escola.substring(1)) // exibe até o índice 1
console.log(escola.substring(0, 3)) // exibe do indice 0 ao 3 (0 1 2)
console.log('Escola '.concat(escola).concat("!")) //concatena
console.log("Escola" + escola + "!") //concatena também
console.log(escola.replace(3, 'e')) // substitui a letra 3 por e
console.log('Ana,Maria,Pedro'.split(',')) // arry (separa por ,)

